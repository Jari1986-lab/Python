def game(board: list):
    player1 = int(0)
    player2 = int(0)
    for i in range(len(board)):
        for j in range(len(board[i])):
            if board[i][j] == 1:
                player1 += 1
            elif board[i][j] == 2:
                player2 += 1
    if player1 > player2:
        return 1
    elif player2 > player1:
        return 2
    else:
        return 0
